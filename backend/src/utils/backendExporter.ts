/**
 * Generates a CRUD route file for a table
 */
function generateRouteFile(tableName: string): string {
  const camelCaseName = tableName.charAt(0).toLowerCase() + tableName.slice(1);
  const PascalCaseName = tableName.charAt(0).toUpperCase() + tableName.slice(1);

  return `import express, { Router, Request, Response } from 'express';
import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();
const router: Router = express.Router();

/**
 * POST /api/${camelCaseName}
 * Creates a new ${camelCaseName}
 */
router.post('/', async (req: Request, res: Response) => {
  try {
    const data = req.body;
    
    if (!data || typeof data !== 'object') {
      res.status(400).json({
        success: false,
        error: 'Request body must be a valid JSON object',
      });
      return;
    }

    const ${camelCaseName} = await prisma.${camelCaseName}.create({
      data,
    });
    
    res.status(201).json({
      success: true,
      data: ${camelCaseName},
    });
  } catch (error: any) {
    res.status(400).json({
      success: false,
      error: error.message || 'Failed to create ${camelCaseName}',
    });
  }
});

/**
 * GET /api/${camelCaseName}
 * Retrieves all ${camelCaseName} records
 */
router.get('/', async (req: Request, res: Response) => {
  try {
    const ${camelCaseName}s = await prisma.${camelCaseName}.findMany();
    
    res.json({
      success: true,
      data: ${camelCaseName}s,
    });
  } catch (error: any) {
    res.status(500).json({
      success: false,
      error: error.message || 'Failed to retrieve ${camelCaseName}s',
    });
  }
});

/**
 * GET /api/${camelCaseName}/:id
 * Retrieves a single ${camelCaseName} by ID
 */
router.get('/:id', async (req: Request, res: Response) => {
  try {
    const { id } = req.params;
    
    if (!id) {
      res.status(400).json({
        success: false,
        error: 'ID parameter is required',
      });
      return;
    }

    const ${camelCaseName} = await prisma.${camelCaseName}.findUnique({
      where: { id },
    });
    
    if (!${camelCaseName}) {
      res.status(404).json({
        success: false,
        error: '${PascalCaseName} not found',
      });
      return;
    }
    
    res.json({
      success: true,
      data: ${camelCaseName},
    });
  } catch (error: any) {
    res.status(500).json({
      success: false,
      error: error.message || 'Failed to retrieve ${camelCaseName}',
    });
  }
});

/**
 * PUT /api/${camelCaseName}/:id
 * Updates an existing ${camelCaseName}
 */
router.put('/:id', async (req: Request, res: Response) => {
  try {
    const { id } = req.params;
    const data = req.body;
    
    if (!id) {
      res.status(400).json({
        success: false,
        error: 'ID parameter is required',
      });
      return;
    }

    if (!data || typeof data !== 'object') {
      res.status(400).json({
        success: false,
        error: 'Request body must be a valid JSON object',
      });
      return;
    }

    const ${camelCaseName} = await prisma.${camelCaseName}.update({
      where: { id },
      data,
    });
    
    res.json({
      success: true,
      data: ${camelCaseName},
    });
  } catch (error: any) {
    res.status(400).json({
      success: false,
      error: error.message || 'Failed to update ${camelCaseName}',
    });
  }
});

/**
 * DELETE /api/${camelCaseName}/:id
 * Deletes a ${camelCaseName}
 */
router.delete('/:id', async (req: Request, res: Response) => {
  try {
    const { id } = req.params;
    
    if (!id) {
      res.status(400).json({
        success: false,
        error: 'ID parameter is required',
      });
      return;
    }

    const ${camelCaseName} = await prisma.${camelCaseName}.delete({
      where: { id },
    });
    
    res.json({
      success: true,
      data: ${camelCaseName},
    });
  } catch (error: any) {
    res.status(400).json({
      success: false,
      error: error.message || 'Failed to delete ${camelCaseName}',
    });
  }
});

export default router;
`;
}

/**
 * Generates a complete backend project for export
 */
export async function generateBackendProject(): Promise<ExportedProject> {
  try {
    // Step 1: Get all tables from the database
    const allTables = await prisma.table.findMany();
    const tableNames = allTables.map((t) => t.name);

    if (tableNames.length === 0) {
      throw new Error('No tables found. Please create at least one table before exporting.');
    }

    // Step 2: Read the current Prisma schema
    const schemaPath = path.join(process.cwd(), 'prisma', 'schema.prisma');
    const schemaContent = fs.readFileSync(schemaPath, 'utf-8');

    // Step 3: Generate all project files
    const files: ExportedProject['files'] = {
      'prisma/schema.prisma': schemaContent,
      'src/server.ts': generateServerTs(tableNames),
      'package.json': generatePackageJson(tableNames),
      'README.md': generateReadme(tableNames),
      '.env': 'DATABASE_URL="file:./prisma/dev.db"\nPORT=5000\n',
      'tsconfig.json': JSON.stringify(
        {
          compilerOptions: {
            target: 'ES2020',
            module: 'commonjs',
            lib: ['ES2020'],
            outDir: './dist',
            rootDir: './src',
            strict: true,
            esModuleInterop: true,
            skipLibCheck: true,
            forceConsistentCasingInFileNames: true,
            resolveJsonModule: true,
          },
          include: ['src'],
          exclude: ['node_modules', 'dist'],
        },
        null,
        2
      ),
    };

    // Step 4: Generate route files for each table
    tableNames.forEach((tableName) => {
      const camelCaseName = tableName.charAt(0).toLowerCase() + tableName.slice(1);
      const routeKey = `src/routes/${camelCaseName}.routes.ts`;
      files[routeKey] = generateRouteFile(tableName);
    });

    return {
      folderPath: path.join(process.cwd(), 'generated-backend'),
      files,
    };
  } catch (error: any) {
    throw new Error(`Failed to generate backend: ${error.message}`);
  }
}
