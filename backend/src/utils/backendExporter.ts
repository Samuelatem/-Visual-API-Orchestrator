import fs from 'fs';
import path from 'path';
import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

export interface ExportedProject {
  folderPath: string;
  files: {
    [filepath: string]: string;
  };
}

/**
 * Generates the server.ts file
 */
function generateServerTs(tableNames: string[]): string {
  const imports = tableNames
    .map((name) => {
      const camelCase = name.charAt(0).toLowerCase() + name.slice(1);
      return `import ${camelCase}Routes from './routes/${camelCase}.routes';`;
    })
    .join('\n');

  const routeRegistrations = tableNames
    .map((name) => {
      const camelCase = name.charAt(0).toLowerCase() + name.slice(1);
      return `app.use('/api/${camelCase}', ${camelCase}Routes);`;
    })
    .join('\n');

  return `import express, { Express, Request, Response } from 'express';
import cors from 'cors';
import { PrismaClient } from '@prisma/client';

${imports}

const app: Express = express();
const port = process.env.PORT || 5000;
const prisma = new PrismaClient();

// Middleware
app.use(cors());
app.use(express.json());

// Routes
${routeRegistrations}

// Health check
app.get('/api/health', (req: Request, res: Response) => {
  res.json({ status: 'ok' });
});

// Start server
app.listen(port, () => {
  console.log(\`Server running at http://localhost:\${port}\`);
});

export default app;
`;
}

